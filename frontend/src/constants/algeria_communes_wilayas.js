const _communesByWilaya = {};
const _wilayaByCode = {};

algeriaWilayas.forEach(w => {
  _wilayaByCode[w.code] = w;
});

algeriaCommunes.forEach(c => {
  const wilaya = algeriaWilayas.find(w => w.name === c.wilaya);
  if (wilaya) {
    if (!_communesByWilaya[wilaya.code]) _communesByWilaya[wilaya.code] = [];
    _communesByWilaya[wilaya.code].push(c.commune);
  }
});

// Sort communes alphabetically per wilaya
Object.values(_communesByWilaya).forEach(arr => arr.sort((a, b) => a.localeCompare(b)));

